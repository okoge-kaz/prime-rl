"""Compute SWE-RL ISL/OSL statistics from traces.jsonl.

One assistant message is one turn:
  ISL       = full conversation length when generation starts
  delta_ISL = input added since the previous turn; approximately the prefill
              work with a prefix-cache hit
  OSL       = tokens generated by the turn, including tool-call arguments

Messages are tokenized independently, so this estimate excludes chat-template
overhead of a few tokens per message.

Usage (CPU-only; safe on a login node):
    uv run python experiments/analysis/isl_osl.py \
        outputs/job-175970/run_default/rollouts/step_1/train/all/traces.jsonl [more...]
"""

import argparse
import json
import os
import statistics
from pathlib import Path

os.environ.setdefault("HF_HUB_CACHE", "/lustre/fsw/portfolios/coreai/users/kfujii/hf_checkpoints/hub")
os.environ.setdefault("HF_HUB_OFFLINE", "1")

MODEL = "Qwen/Qwen3-30B-A3B-Thinking-2507"


def msg_tokens(tok, m) -> int:
    text = m.get("content") or ""
    for tc in m.get("tool_calls") or []:
        text += (tc.get("name") or "") + (tc.get("arguments") or "")
    return len(tok.encode(text, add_special_tokens=False)) if text else 0


def pct(v, q):
    v = sorted(v)
    return v[min(int(len(v) * q), len(v) - 1)]


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("traces", nargs="+", type=Path)
    parser.add_argument("--model", default=MODEL)
    args = parser.parse_args()

    from transformers import AutoTokenizer

    tok = AutoTokenizer.from_pretrained(args.model)

    isl, disl, osl = [], [], []          # Per turn.
    r_ctx, r_osl, r_turns = [], [], []   # Per rollout.
    for path in args.traces:
        for line in path.open():
            r = json.loads(line)
            ctx = 0
            last_ctx_at_gen = 0
            total_out = 0
            turns = 0
            for n in r["nodes"]:
                m = n["message"]
                t = msg_tokens(tok, m)
                if m["role"] == "assistant":
                    # Qwen counts reasoning as generated output but drops prior
                    # reasoning from the context rendered for later turns.
                    think = m.get("reasoning_content") or ""
                    gen = t + (len(tok.encode(think, add_special_tokens=False)) if think else 0)
                    isl.append(ctx)
                    disl.append(ctx - last_ctx_at_gen)
                    osl.append(gen)
                    last_ctx_at_gen = ctx + t
                    total_out += gen
                    turns += 1
                ctx += t
            if turns:
                r_ctx.append(ctx)
                r_osl.append(total_out)
                r_turns.append(turns)

    def row(name, v):
        print(f"{name:<28}{statistics.mean(v):>10.0f}{pct(v, 0.5):>10}{pct(v, 0.9):>10}{max(v):>10}")

    print(f"rollouts={len(r_ctx)}  turns={len(isl)}  (tokenizer={args.model})")
    print(f"{'per-turn metric':<28}{'mean':>10}{'p50':>10}{'p90':>10}{'max':>10}")
    row("ISL (context at gen)", isl)
    row("delta-ISL (new prefill)", disl)
    row("OSL (generated)", osl)
    print(f"{'per-rollout metric':<28}{'mean':>10}{'p50':>10}{'p90':>10}{'max':>10}")
    row("final context", r_ctx)
    row("total generated", r_osl)
    row("turns", r_turns)
    print()
    print(f"ISL:OSL (no cache, per turn)      = {sum(isl) / sum(osl):.2f} : 1")
    print(f"delta-ISL:OSL (with prefix cache) = {sum(disl) / sum(osl):.2f} : 1")
    print(f"prefill token share w/ cache      = {sum(disl) / (sum(disl) + sum(osl)) * 100:.1f}%")


if __name__ == "__main__":
    main()
